package bytedatatype;

public class bytedatatype {
    public static void main(String[] args) {
        byte a = 11, b = 10;
        byte add = (byte) (a + b);
        byte sub = (byte) (a - b);
        byte multi = (byte) (a * b);
        byte divide = (byte) (a / b);
        System.out.println("\nAddition: " + add);
        System.out.println("\nSubtraction: " + sub);
        System.out.println("\nMultiplication: " + multi);
        System.out.println("\nDivision: " + divide);
    }
}
